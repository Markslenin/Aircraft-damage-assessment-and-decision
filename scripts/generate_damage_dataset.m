function dataset = generate_damage_dataset()
%GENERATE_DAMAGE_DATASET Generate a P1 summary dataset for downstream ML.

rootDir = fileparts(fileparts(mfilename('fullpath')));
run(fullfile(rootDir, 'scripts', 'init_project.m'));

summaryPath = fullfile(rootDir, 'results', 'batch_run_summary.mat');
if ~isfile(summaryPath)
    run_batch_scenarios();
end

S = load(summaryPath, 'summary');
summary = S.summary;
Pcfg = evalin('base', 'P');

dataset = struct('theta_d', {}, 'initialCondition', {}, 'summaryFeatures', {}, ...
    'ctrlMetrics', {}, 'trimInfo', {}, 'decisionOutput', {});

for i = 1:numel(summary)
    entry = struct();
    entry.theta_d = summary(i).theta_d;
    entry.initialCondition = Pcfg.initial;
    entry.summaryFeatures = struct( ...
        'initialAirspeed_mps', norm(Pcfg.initial.uvw_mps), ...
        'initialAltitude_m', -Pcfg.initial.pned_m(3), ...
        'initialEuler_rad', Pcfg.initial.euler_rad, ...
        'simSuccess', summary(i).simSuccess, ...
        'simFinalTime_s', summary(i).simFinalTime_s, ...
        'eta_total', summary(i).ctrlMetrics.eta_total, ...
        'dragScale', summary(i).damageEffects.dragScale, ...
        'liftScale', summary(i).damageEffects.liftScale, ...
        'momentBiasNorm', norm([ ...
            summary(i).damageEffects.rollMomentBias; ...
            summary(i).damageEffects.pitchMomentBias; ...
            summary(i).damageEffects.yawMomentBias]), ...
        'controlScaleMean', mean([ ...
            summary(i).damageEffects.aileronEffScale, ...
            summary(i).damageEffects.elevatorEffScale, ...
            summary(i).damageEffects.rudderEffScale, ...
            summary(i).damageEffects.thrustEffScale]));
    entry.ctrlMetrics = summary(i).ctrlMetrics;
    entry.trimInfo = summary(i).trimInfo;
    entry.decisionOutput = summary(i).decisionOutput;

    % TODO: Extend summaryFeatures with full state histories once the model
    % exposes logged top-level state traces suitable for dataset export.
    dataset(i) = entry; %#ok<AGROW>
end

save(fullfile(rootDir, 'data', 'damage_dataset.mat'), 'dataset');
fprintf('Damage dataset saved with %d samples.\n', numel(dataset));
end
